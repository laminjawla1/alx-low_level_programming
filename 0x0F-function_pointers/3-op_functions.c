#include "3-cal.h"
/**
*op_add - adds two integers
*
*@a: num1
*@b: num2
*
*Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - subtract two integers
*
*@a: num1
*@b: num2
*
*Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - multiplies two integers
*
*@a: num1
*@b: num2
*
*Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - divides two integers
*
*@a: num1
*@b: num2
*
*Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - finds the remainder after dividing two integers
*
*@a: num1
*@b: num2
*
*Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
