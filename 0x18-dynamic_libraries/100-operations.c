/**
* add - Adds two integers
*
*@a: Operand one
*@b: Operand two
*
*Return: a + b
*/
int add(int a, int b)
{
	return (a + b);
}
/**
* sub - Subtract two integers
*
*@a: Operand one
*@b: Operand two
*
*Return: a - b
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
* mul - Multiplies two integers
*
*@a: Operand one
*@b: Operand two
*
*Return: a * b
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
* div - Divides two integers
*
*@a: Operand one
*@b: Operand two
*
*Return: a / b
*/
int div(int a, int b)
{
	return (a / b);
}
/**
* mod - gets the mod of two integers
*
*@a: Operand one
*@b: Operand two
*
*Return: a % b
*/
int mod(int a, int b)
{
	return (a % b);
}
